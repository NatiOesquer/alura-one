
//punto 1: Contador de 1 a 10 con bucle 'while'

let contador1 = 1;

while (contador1 <= 10) {
  console.log(contador1);
  contador1++;
}


//punto 2: Contador de 10 a 0 con bucle 'while'

let contador2 = 10;

while (contador2 >= 0) {
  console.log(contador2);
  contador2--;
}



//punto 3:  Programa de cuenta regresiva

let contadorRegresivo = prompt("Ingrese un número para la cuenta regresiva:");


while (contadorRegresivo >= 0) {
  console.log(contadorRegresivo);
  contadorRegresivo--;
}


//punto 4: Programa de cuenta progresiva

let limiteProgresivo = prompt("Ingrese un número para la cuenta progresiva:");
let contadorProgresivo = 0;

while (contadorProgresivo <= limiteProgresivo) {
  console.log(contadorProgresivo);
  contadorProgresivo++;
}







